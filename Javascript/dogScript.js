function imagecarousel1() {
    if (document.getElementById("section1image1").style.display !== "none") {
    document.getElementById("section1image1").style.display = "none";
    document.getElementById("section1image2").style.display = "block";
    document.getElementById("section1image3").style.display = "none";
    } 
    else if (document.getElementById("section1image2").style.display !== "none") {
    document.getElementById("section1image1").style.display = "none";
    document.getElementById("section1image2").style.display = "none";
    document.getElementById("section1image3").style.display = "block";
    } 
    else {
    document.getElementById("section1image1").style.display = "block";
    document.getElementById("section1image2").style.display = "none";
    document.getElementById("section1image3").style.display = "none";
    }
}
